def приветствие(имя):
  """Этот метод выводит приветствие с указанным именем."""
  print(f"Привет, {имя}!")

приветствие("Мир") 
class Кот:
  """Класс, описывающий кота."""

  def __init__(self, имя, цвет):
    """Инициализация кота с именем и цветом."""
    self.имя = имя
    self.цвет = цвет

  def мяукать(self):
    """Метод, имитирующий мяуканье кота."""
    print("Мяу!")

  def описание(self):
    """Метод, выводящий описание кота."""
    print(f"Это кот по имени {self.имя}, он {self.цвет} цвета.")


мой_кот = Кот("Барсик", "рыжий")
мой_кот.мяукать()
мой_кот.описание() 
class ДомашнийКот(Кот):
  """Класс, описывающий домашнего кота, унаследованный от Кот."""

  def __init__(self, имя, цвет, любимая_игрушка):
    """Инициализация домашнего кота с именем, цветом и любимой игрушкой."""
    super().__init__(имя, цвет)  
    self.любимая_игрушка = любимая_игрушка

  def играть(self):
    """Метод, имитирующий игру кота."""
    print(f"{self.имя} играет с {self.любимая_игрушка}!")

мой_домашний_кот = ДомашнийКот("Мурзик", "серый", "клубок")

мой_домашний_кот.мяукать()  
мой_домашний_кот.описание() 
мой_домашний_кот.играть()
